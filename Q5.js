console.log("Ascending Order:");
let ascendingNumber = 1; // Starting number
while (ascendingNumber <= 25) {
  console.log(ascendingNumber);
  ascendingNumber ++; // Increment by 1 for next number
}

// Print numbers from 25 to 1 in descending order
console.log("\nDescending Order:");
let descendingNumber = 25; // Starting number
while (descendingNumber >= 1) {
  console.log(descendingNumber);
  descendingNumber--; // Decrement by 1 for next number
}